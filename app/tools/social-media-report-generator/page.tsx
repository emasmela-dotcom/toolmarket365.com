function toNumber(value: string): number {
  const n = Number(value.replace(/,/g, '').trim())
  return Number.isFinite(n) ? n : 0
}

function pct(n: number): string {
  if (!Number.isFinite(n)) return '0.00%'
  return `${n.toFixed(2)}%`
}

function money(n: number): string {
  if (!Number.isFinite(n)) return '$0.00'
  return `$${n.toFixed(2)}`
}

